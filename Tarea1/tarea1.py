"""üìò Ejercicio 3: Cree un programa que elimine los elementos repetidos de una
 lista y devuelva una nueva lista con elementos √∫nicos ordenados.
"""

lista = [5, 8, 3, 5, 2, 8, 9, 1, 3, 4, 5, 9, 7, 2, 6, 8]
miListaSinDuplicados=[]
vistos=set()

for numero in lista:
  if miListaSinDuplicados not in vistos:
    miListaSinDuplicados.append(numero)
    vistos.add(numero)

print(miListaSinDuplicados)

